import {
  existsSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  writeFileSync,
} from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

import matter from 'gray-matter'
import { remark } from 'remark'
import html from 'remark-html'

const SLUG_PATTERN = /^[a-z0-9-]+$/

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const projectRoot = path.resolve(scriptDir, '..')
const blogDir = path.join(projectRoot, 'src/content/blog')
const manifestPath = path.join(blogDir, 'manifest.ts')

function requireStringField(frontmatter, fieldName, slug) {
  const value = frontmatter[fieldName]

  if (typeof value !== 'string' || !value.trim()) {
    throw new Error(
      `Blog post "${slug}" is missing required frontmatter field "${fieldName}"`
    )
  }

  return value.trim()
}

function requireDateField(frontmatter, slug) {
  const value = frontmatter.date

  if (value instanceof Date && !Number.isNaN(value.getTime())) {
    return value.toISOString().slice(0, 10)
  }

  if (typeof value === 'string' && value.trim()) {
    return value.trim()
  }

  throw new Error(
    `Blog post "${slug}" is missing required frontmatter field "date"`
  )
}

function parseTags(tags) {
  if (Array.isArray(tags)) {
    return tags
      .filter((tag) => typeof tag === 'string')
      .map((tag) => tag.trim())
      .filter(Boolean)
  }

  if (typeof tags === 'string') {
    return tags
      .split(',')
      .map((tag) => tag.trim())
      .filter(Boolean)
  }

  return []
}

function parsePost(fileName) {
  const slug = fileName.replace(/\.md$/, '')

  if (!SLUG_PATTERN.test(slug)) {
    throw new Error(
      `Blog post file "${fileName}" must use a lowercase kebab-case slug`
    )
  }

  const source = readFileSync(path.join(blogDir, fileName), 'utf8')
  const { content, data } = matter(source)
  const contentHtml = remark().use(html).processSync(content).toString()

  return {
    slug,
    title: requireStringField(data, 'title', slug),
    description: requireStringField(data, 'description', slug),
    date: requireDateField(data, slug),
    tags: parseTags(data.tags),
    contentHtml,
  }
}

function buildManifest() {
  mkdirSync(blogDir, { recursive: true })

  const posts = existsSync(blogDir)
    ? readdirSync(blogDir)
        .filter((fileName) => fileName.endsWith('.md'))
        .sort()
        .map(parsePost)
    : []

  return [
    '// This file is generated by scripts/generate-blog-manifest.mjs.',
    '// Do not edit by hand; edit Markdown files in this directory instead.',
    '',
    'export type BlogPostManifestEntry = {',
    '  slug: string',
    '  title: string',
    '  description: string',
    '  date: string',
    '  tags: string[]',
    '  contentHtml: string',
    '}',
    '',
    '// prettier-ignore',
    `export const blogPosts: BlogPostManifestEntry[] = ${JSON.stringify(
      posts,
      null,
      2
    )}`,
    '',
  ].join('\n')
}

const manifest = buildManifest()
const previousManifest = existsSync(manifestPath)
  ? readFileSync(manifestPath, 'utf8')
  : null

if (previousManifest !== manifest) {
  writeFileSync(manifestPath, manifest)
}
